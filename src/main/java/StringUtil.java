public class StringUtil {

    public String low(String str) {
        return str.toLowerCase();
    }

    public String up(String str) {
        return str.toUpperCase();
    }

    public int size(String str) {
        return str.length();
    }

    public String unite(String s1, String s2) {
        return (s1 + s2);
    }
}
